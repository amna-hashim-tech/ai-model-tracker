export const connections = (() => {
  const arcs = [];
  companies.forEach((company) => {
    researchCenters.forEach((center) => {
      const dist = Math.sqrt(
        Math.pow(company.lat - center.lat, 2) + Math.pow(company.lng - center.lng, 2)
      );
      if (dist > 10 && Math.random() > 0.5) {
        arcs.push({
          startLat: company.lat,
          startLng: company.lng,
          endLat: center.lat,
          endLng: center.lng,
          color: company.color,
          company: company.name,
          center: center.name,
        });
      }
    });
  });
  return arcs;
})();

